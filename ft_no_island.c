#include "fillit.h"

int		ft_no_island(const char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '#')
		{
			if ((str[i + 1] == '#') || (str[i + 5] == '#'))
				j++;
			else if ((str[i - 1] == '#') && (str[i + 4] == '#'))
				j++;
			else if (((str[i - 5] == '#') || (str[i - 1] == '#')) && (j == 3))
				j++;
			else if ((str[i - 1] == '#') && (str[i + 3] == '#') && (j == 2))
				j++;
		}
		i++;
	}
	if (j == 4)
		return (1);
	else
		return (0);
}
