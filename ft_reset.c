#include "fillit.h"

char	*ft_reset(char *str, char *c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (c[j] == '.')
		j++;
	while (str[i] != '\0')
	{
		if ((str[i] != '.') && (str[i] != '\n'))
			if ((str[i] == c[j]) || (c[j] == '*'))
				str[i] = '.';
		i++;
	}
	return (str);
}
