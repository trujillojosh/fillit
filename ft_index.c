#include "fillit.h"

int		ft_index(char *s, int i, int pos)
{
	if ((s[i + 5] == '#') && (s[i + 9] == '#') && (s[i + 10] == '#'))
		pos = pos - 1;
	else if (s[i + 4] == '#')
	{
		if ((s[i + 5] == '#') && (s[i + 10] == '#'))
			pos = pos - 1;
		else if ((s[i + 3] == '#') && (s[i + 5] == '#'))
			pos = pos - 2;
		else if ((s[i + 5] == '#') && (s[i + 9] == '#'))
			pos = pos - 1;
		else if ((s[i + 5] == '#') && (s[i + 1] == '#'))
			pos = pos - 1;
		else if ((s[i + 5] == '#') && (s[i + 6] == '#'))
			pos = pos - 1;
	}
	return (pos);
}
